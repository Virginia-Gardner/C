#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	j;

	if (argc > 1)
	{
		j = 1;
		while (j < argc)
		{
			i = 0;
			while (argv[j][i])
				i++;
			write(1, argv[j], i);
			write(1, "\n", 1);
			j++;
		}
	}	
}
