#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	j;

	if (argc > 1)
	{
		while (argc > 1)
		{
			i = 0;
			while (argv[argc - 1][i])
				i++;
			write(1, argv[argc - 1], i);
			write(1, "\n", 1);
			argc--;
		}		
	}
}
