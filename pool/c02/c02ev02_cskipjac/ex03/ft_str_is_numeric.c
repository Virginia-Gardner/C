int	ft_str_is_numeric(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 1;
	if (str[i] != '\0')
	{
		while (str[i] != '\0')
		{
			if (str[i] >= 0 & str[i] < 48)
				j = 0;
			if (str[i] >= 58 & str[i] <= 127)
				j = 0;
			i++;
		}
	}	
	return (j);
}
