int	ft_sqrt(int nb)
{
	int	i;
	int	nb2;

	i = 1;
	nb2 = nb / 2 + 1;
	if (nb2 > 46341)
		nb2 = 46341;
	while (i * i <= nb2)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
