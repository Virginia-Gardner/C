int	ft_is_prime(int nb)
{
	int	i;
	int	nb2;

	i = 2;
	nb2 = nb / 2 + 1;
	if (nb < 2)
		return (0);
	while (i < nb2)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
